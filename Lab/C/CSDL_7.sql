  SELECT MAX(DIEM) FROM KETQUA
  WHERE LANTHI=1
) AND KQ.MASV=SV.MASV
--6.3
SELECT HOSV,TENSV,YEAR(GETDATE())-YEAR(NGAYSINH) AS TUOI
FROM DMSV AS SV,DMKHOA AS K
WHERE YEAR(NGAYSINH)=(
  SELECT MIN(YEAR(NGAYSINH)) FROM DMSV,DMKHOA
  WHERE SV.MAKHOA= K.MAKHOA AND TENKHOA LIKE N'ANH VĂN'
) 
--6.4
SELECT HOSV,TENSV
FROM DMSV AS SV ,KETQUA AS KQ
WHERE DIEM=(
  SELECT MIN(DIEM)
  FROM KETQUA AS KQ,DMMH AS MH,DMKHOA AS K,DMSV AS SV
  WHERE KQ.MASV=SV.MASV AND SV.MAKHOA=K.MAKHOA 
        AND KQ.MAMH=MH.MAMH 
        AND TENKHOA LIKE N'ANH VĂN' 
        AND TENMH LIKE N'VĂN PHẠM'
        AND LANTHI=1
)

--6.5
SELECT HOSV,TENSV
FROM DMSV AS SV,KETQUA AS KQ,DMMH AS MH
WHERE DIEM=(
  SELECT DIEM
  FROM KETQUA AS KQ,DMSV AS SV,DMMH AS MH
  WHERE KQ.MASV=SV.MASV AND KQ.MAMH=MH.MAMH AND LANTHI=1 AND TENMH LIKE N'CƠ SỞ DỮ LIỆU' AND SV.MASV='A01'
) AND KQ.MASV=SV.MASV AND KQ.MAMH=MH.MAMH AND LANTHI=2 AND TENMH LIKE N'CƠ SỞ DỮ LIỆU' AND SV.MASV!='A01'
-- 6.6
SELECT HOSV,TENSV
FROM DMSV AS SV,KETQUA AS KQ,DMMH AS MH,DMKHOA AS K
WHERE DIEM>(
  SELECT DIEM
  FROM KETQUA AS KQ,DMSV AS SV,DMMH AS MH,DMKHOA AS K
  WHERE SV.MAKHOA=K.MAKHOA AND KQ.MASV=SV.MASV AND KQ.MAMH=MH.MAMH AND TENMH LIKE N'CƠ SỞ DỮ LIỆU' AND TENKHOA LIKE N'ANH VĂN'
) AND SV.MAKHOA=K.MAKHOA AND KQ.MASV=SV.MASV AND KQ.MAMH=MH.MAMH  AND TENMH LIKE N'VĂN PHẠM' AND TENKHOA NOT LIKE N'ANH VĂN'
