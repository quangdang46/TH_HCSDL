CREATE FUNCTION GETDIEMTB(@MASV VARCHAR(255))
RETURNS FLOAT
AS
BEGIN
	DECLARE @DIEMTONG FLOAT
	SET @DIEMTONG=0
	DECLARE @TONGSOMON INT

	SELECT @TONGSOMON=COUNT(DISTINCT LANTHI)
	FROM KETQUA
	WHERE @MASV=MASV
	GROUP BY MASV


	SELECT @DIEMTONG+=DBO.GETDIEM(@MASV,MAMH)
	FROM KETQUA
	WHERE @MASV=MASV
	GROUP BY MASV,MAMH

	RETURN @DIEMTONG/@TONGSOMON



END

GO
